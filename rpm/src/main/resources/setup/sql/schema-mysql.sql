CREATE TABLE IF NOT EXISTS `inventory` (
	`id` int NOT NULL AUTO_INCREMENT,
	`gtin` varchar(14),
	`product_type` varchar(256),
	`style_code` varchar(64) NOT NULL,
	`color_code` varchar(64),
	`color_description` varchar(64),
	`color_type` varchar(64),
	`size_code` varchar(64),
	`brand` varchar(256),
	`gender` varchar(64),
	`fit` varchar(64),
	`price` DECIMAL(6,2),
	`price_map` DECIMAL(6,2),
	`collection` varchar(256),
	`description_short` TEXT,
	`description_long` TEXT,
	`callouts` TEXT,
	`image_1` TEXT,
	`image_2` TEXT,
	`stock` int,
	`weight` DECIMAL(6,2),
	`supplier` varchar(64) NOT NULL,
	`source_input_name` varchar(256) NOT NULL,
	`source_input_id` int NOT NULL,
	PRIMARY KEY (`id`)
) ENGINE=Aria;

CREATE TABLE IF NOT EXISTS `products` (
	`id` int NOT NULL AUTO_INCREMENT,
	`gtin` varchar(14) NOT NULL UNIQUE,
	`sku` varchar(192) NOT NULL UNIQUE,
	`name` varchar(256) NOT NULL,
	`short_description` TEXT NOT NULL,
	`description` TEXT NOT NULL,
	`categories` TEXT,
	`url_key` varchar(192) NOT NULL,
	`price` DECIMAL(6,2) NOT NULL,
	`image` TEXT NOT NULL,
	`image_label` TEXT NOT NULL,
	`product_online` tinyint NOT NULL DEFAULT '0',
	`attribute_set_code` varchar(256) NOT NULL DEFAULT 'Default',
	`additional_attributes` TEXT,
	`weight` DECIMAL(6,2) NOT NULL,
	`qty` int NOT NULL,
	`source_input_name` varchar(256) NOT NULL,
	`source_input_id` int NOT NULL,
	PRIMARY KEY (`id`)
) ENGINE=Aria;
